int	ft_sqrt(int nb)
{
	int	x;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb > 2)
	{
		x = 2;
		while (x * x < nb && x < 46341)
			x++;
		if (x * x == nb)
			return (x);
	}
	return (0);
}
